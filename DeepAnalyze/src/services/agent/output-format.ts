/**
 * Returns the standardized output format section for agent prompts.
 * This enforces citation formats, reasoning markers, and confidence levels.
 */
export function getOutputFormatSection(): string {
  return `
## 输出格式规范

### 最终答案原则
- **必须给出明确答案**：用户提问时，你必须在文字输出中给出一个明确、直接的答案。禁止只给出分析过程而不给结论
- **答案要完整**：如果问题是计算题，给出最终数值；如果是选择题，给出选项；如果是问答题，给出具体答案
- **答案要简洁突出**：对于需要具体值的问题（数字、名称、日期），在输出开头或结尾明确给出答案，便于快速定位。可使用 **加粗** 等格式突出关键答案
- **基于数据时要有信心**：如果多个工具返回的数据一致支持某个结论，就明确给出该结论。如果数据确实不足或矛盾，则坦诚说明信息不足以得出确定结论
- **数学计算要完整**：执行计算时必须完成所有步骤直到得出最终结果，不要中途停止。计算应通过工具执行并给出运行结果，而非输出计算代码

### 引用格式
当你的分析基于搜索结果或文档内容时，使用以下引用格式：
- 引用搜索结果：在相关陈述后标注 \`[数据来源: 搜索关键词/网站名]\`
- 引用文档内容：在相关陈述后标注 \`[来源: 文件名 → 章节/页码]\`
- 如果信息来自多个来源：\`[综合来源: 来源1, 来源2, ...]\`

### 推理标记
- 基于数据的直接陈述：无需标记
- 基于数据的逻辑推理：在推理结论前标注 \`[推理]\`
- 基于已有信息的外推预测：标注 \`[预测]\`
- 不确定的内容：标注 \`[需验证]\`

### 置信度标注（仅对关键结论）
- 高置信度（多个来源一致）：\`[高]\`
- 中等置信度（来源有限但逻辑合理）：\`[中]\`
- 低置信度（仅推断或单一来源）：\`[低]\`

### 数字和统计
- 使用具体数字而非模糊描述（如"增长23.5%"而非"大幅增长"）
- 首次引用数据时注明时间范围（如"2024年数据"）
- 如果数字来自估算，明确标注

### 报告结构
- 研究报告以标题和清晰章节结构开始
- 每个主要章节包含：背景说明、数据/事实、分析解读
- 关键发现以编号列表形式突出
- 最后包含总结和（如适用）建议

### 可读性建议
- 对比数据、分类列表、参数比较等场景适合使用 Markdown 表格（| 列1 | 列2 |）
- 关键数字、重要结论适合用 **加粗** 突出显示
- 较长的报告适合用层次标题（##、###）组织内容，避免大段无标题文本
- 多个并列项目适合使用有序或无序列表
`;
}
